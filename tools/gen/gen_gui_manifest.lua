Same reasoning as tools/gen/gen_userland_manifest.lua. gui/xmake.lua's own
parse_modules_list() used io.open() directly at description scope to read
gui/MODULES (~200 lines, category/name + upstream version per X.Org
module) -- confirmed nil there in a real xmake v3.0.9 run. This script
does the same parse via `xmake lua` (where io really works) and writes the
result to gui/generated_manifest.lua.

Regenerate with: xmake lua tools/gen/gen_gui_manifest.lua
(only needed again if gui/MODULES itself changes -- upstream module
versions, additions, removals.)
--------------------------------------------------------------------------------
--]]

local ROOT = path.absolute(os.scriptdir() .. "/../..")
local GUI  = path.join(ROOT, "gui")

local function parse_modules_list(filepath)
    if not os.isfile(filepath) then
        print(string.format("eteleos-gen-gui: MODULES file not found at %s", filepath))
        return {}
    end
    local f = io.open(filepath, "r")
    if not f then return {} end
    local modules = {}
    for line in f:lines() do
        local stripped = line:gsub("#.*$", "")
        local relpath, version = stripped:match("^(%S+)%s+(%S+)")
        if relpath then
            local category, name = relpath:match("^([^/]+)/(.+)$")
            if category and name then
                modules[#modules + 1] = { category = category, name = name,
                                           relpath = relpath, version = version }
            end
        end
    end
    f:close()
    return modules
end

local all_modules = parse_modules_list(path.join(GUI, "MODULES"))
print(string.format("eteleos-gen-gui: %d modules discovered from MODULES", #all_modules))

local function lua_quote(s) return string.format("%q", s) end

local out = {}
out[#out + 1] = "--[[ AUTO-GENERATED by tools/gen/gen_gui_manifest.lua -- DO NOT EDIT BY HAND."
out[#out + 1] = "     Regenerate with: xmake lua tools/gen/gen_gui_manifest.lua ]]"
out[#out + 1] = ""
out[#out + 1] = "ETELEOS_GUI_MANIFEST = {"
for _, m in ipairs(all_modules) do
    out[#out + 1] = string.format("    { category = %q, name = %q, relpath = %q, version = %q },",
        m.category, m.name, m.relpath, m.version or "")
end
out[#out + 1] = "}"
out[#out + 1] = ""

local outpath = path.join(GUI, "generated_manifest.lua")
local f = io.open(outpath, "w")
f:write(table.concat(out, "\n"))
f:close()
print(string.format("eteleos-gen-gui: manifest written to %s", outpath))
