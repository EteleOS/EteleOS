Same reasoning as tools/gen/gen_userland_manifest.lua (see that file's
header for the full explanation, confirmed against a real xmake v3.0.9
build, of why io.open cannot run at xmake.lua description scope).

libraries/xmake.lua does NOT dynamically discover which libraries exist --
it has an explicit, hardcoded eteleos_bsd_library(name, srcdir, opts) call
per library. The only file-content read it needs is each library's
shlib_version file (major/minor for the shared-object soname), previously
done in-line via read_shlib_version() at description scope, which cannot
work. This script precomputes that lookup table for every directory in
libraries/core and libraries/extra that has a shlib_version file, keyed by
srcdir path relative to libraries/.

Regenerate with: xmake lua tools/gen/gen_libraries_manifest.lua
(or: xmake eteleos-regen-libraries, once a project is configured)
--------------------------------------------------------------------------------
--]]

local ROOT       = path.absolute(os.scriptdir() .. "/../..")
local LIBRARIES  = path.join(ROOT, "libraries")

local function read_file(filepath)
    if not os.isfile(filepath) then return nil end
    local f = io.open(filepath, "r")
    if not f then return nil end
    local c = f:read("*a")
    f:close()
    return c
end

local VERSIONS = {}   -- srcdir_rel (relative to libraries/) -> {major=N, minor=N}

local function scan(base)
    if not os.isdir(base) then return end
    for _, dir in ipairs(os.dirs(path.join(base, "*"))) do
        local vfile = path.join(dir, "shlib_version")
        local content = read_file(vfile)
        if content then
            local major = tonumber(content:match("major%s*=%s*(%d+)")) or 0
            local minor = tonumber(content:match("minor%s*=%s*(%d+)")) or 0
            local rel = path.relative(dir, LIBRARIES)
            VERSIONS[rel] = { major = major, minor = minor }
        end
    end
end

scan(path.join(LIBRARIES, "core"))
scan(path.join(LIBRARIES, "extra"))

local function serialize(t)
    local keys = {}
    for k in pairs(t) do keys[#keys + 1] = k end
    table.sort(keys)
    local parts = {}
    for _, k in ipairs(keys) do
        parts[#parts + 1] = string.format("    [%q] = { major = %d, minor = %d },",
            k, t[k].major, t[k].minor)
    end
    return "{\n" .. table.concat(parts, "\n") .. "\n}"
end

local out = {}
out[#out + 1] = "--[[ AUTO-GENERATED by tools/gen/gen_libraries_manifest.lua -- DO NOT EDIT BY HAND."
out[#out + 1] = "     Regenerate with: xmake lua tools/gen/gen_libraries_manifest.lua ]]"
out[#out + 1] = ""
out[#out + 1] = "ETELEOS_LIBRARIES_SHLIB_VERSIONS = " .. serialize(VERSIONS)
out[#out + 1] = ""

local outpath = path.join(LIBRARIES, "generated_manifest.lua")
local f = io.open(outpath, "w")
f:write(table.concat(out, "\n"))
f:close()

local count = 0
for _ in pairs(VERSIONS) do count = count + 1 end
print(string.format("eteleos-gen: libraries manifest written to %s\n  %d shlib_version files found",
      outpath, count))
